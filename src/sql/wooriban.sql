--팀별 팀원수
SELECT TZ.TEAM_NAME 팀, COUNT(*) 팀원수 
FROM TEAMZ TZ
    JOIN TEAMW TW
    ON TZ.TEAM_ID LIKE TW.TEAM_ID
GROUP BY TZ.TEAM_NAME
ORDER BY TZ.TEAM_NAME;

SELECT (SELECT TEAM_NAME 
        FROM TEAMZ
        WHERE TEAM_ID LIKE TZ.TEAM_ID) 팀명
        , COUNT(*) 팀원수 
FROM TEAMZ TZ
    JOIN TEAMW TW
    ON TZ.TEAM_ID LIKE TW.TEAM_ID
GROUP BY TZ.TEAM_ID
ORDER BY TZ.TEAM_ID;

--SUM()팀원 나이합
SELECT (SELECT TEAM_NAME 
        FROM TEAMZ
        WHERE TEAM_ID LIKE TZ.TEAM_ID) 팀명, SUM(TW.MEM_AGE) "나이합"
FROM TEAMZ TZ
    JOIN TEAMW TW
    ON TZ.TEAM_ID LIKE TW.TEAM_ID
GROUP BY TZ.TEAM_ID
;

--MAX()팀원 나이최대치
SELECT (SELECT TEAM_NAME 
        FROM TEAMZ
        WHERE TEAM_ID LIKE TZ.TEAM_ID) 팀명, MAX(TW.MEM_AGE) "나이최대"
FROM TEAMZ TZ
    JOIN TEAMW TW
    ON TZ.TEAM_ID LIKE TW.TEAM_ID
GROUP BY TZ.TEAM_ID;

--MIN()팀원나이최소치
SELECT (SELECT TEAM_NAME 
        FROM TEAMZ
        WHERE TEAM_ID LIKE TZ.TEAM_ID) 팀명, MIN(TW.MEM_AGE) "나이최소"
FROM TEAMZ TZ
    JOIN TEAMW TW
    ON TZ.TEAM_ID LIKE TW.TEAM_ID
GROUP BY TZ.TEAM_ID;

--AVG()팀원 나이평균
SELECT (SELECT TEAM_NAME 
        FROM TEAMZ
        WHERE TEAM_ID LIKE TZ.TEAM_ID) 팀명, AVG(TW.MEM_AGE) "나이평균"
FROM TEAMZ TZ
    JOIN TEAMW TW
    ON TZ.TEAM_ID LIKE TW.TEAM_ID
GROUP BY TZ.TEAM_ID;

-- 다 합쳐서
SELECT (SELECT TEAM_NAME 
        FROM TEAMZ
        WHERE TEAM_ID LIKE TZ.TEAM_ID) 팀명,
        COUNT(*) 팀원수,
        SUM(TW.MEM_AGE) "나이합",
        MAX(TW.MEM_AGE) "나이최대",
        MIN(TW.MEM_AGE) "나이최소",
        AVG(TW.MEM_AGE) "나이평균"
FROM TEAMZ TZ
    JOIN TEAMW TW
        ON TZ.TEAM_ID LIKE TW.TEAM_ID
GROUP BY TZ.TEAM_ID

ORDER BY TZ.TEAM_ID;

